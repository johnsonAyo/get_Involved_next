/**
 * Get all LGAs for a given state id.
 * @param {string} stateId - e.g. "lagos"
 * @returns {string[]}
 */
export function getLgas(stateId) {
  const state = nigeriaGeo.find((s) => s.id === stateId);
  return state ? state.lgas : [];
}

/**
 * Get state metadata by id.
 * @param {string} stateId
 * @returns {object|undefined}
 */
export function getState(stateId) {
  const state = nigeriaGeo.find((s) => s.id === stateId);
  if (!state) return undefined;
  return {
    ...state,
    slogan: stateSlogans[state.id] || "",
    population: statePopulations2006[state.id] || 0,
    lgaCount: state.lgas.length
  };
}
